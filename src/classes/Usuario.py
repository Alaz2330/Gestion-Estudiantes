class Usuario:
    def __init__(self, user: str, id: str, psd: str, nombre: str, edad: str, genero: str, direccion: str, cnumber: str, email: str):
        self.user = user
        self.id = id
        self.psd = psd
        self.nombre = nombre
        self.edad = edad 
        self.genero = genero 
        self.direccion = direccion
        self.cnumber = cnumber
        self.email = email